    var RADIUS=3;
    var WinWidth=600;
    var WinHeight=300;
    
    function checkTime (i) {
        if (i<10) {
            return "0"+i;
        } else{
            return i;
        }
        return i;
    };
    
    function render(cxt){
        var nowTimec1=new Date();       
        var h=checkTime(nowTimec1.getHours());
        var hArrays=h.toString().split("");     
        var m=checkTime(nowTimec1.getMinutes());
        var mArrays=m.toString().split("");
        var s=checkTime(nowTimec1.getSeconds());
        var sArrays=s.toString().split("");
        cxt.clearRect(0,0,WinWidth,WinHeight);
        renderDigit(60,30,hArrays[0],cxt);
        renderDigit(60+15*(RADIUS+1),30,hArrays[1],cxt);
        renderDigit(60+30*(RADIUS+1),30,10,cxt);
        renderDigit(60+39*(RADIUS+1),30,mArrays[0],cxt);
        renderDigit(60+54*(RADIUS+1),30,mArrays[1],cxt);
        renderDigit(60+69*(RADIUS+1),30,10,cxt);
        renderDigit(60+78*(RADIUS+1),30,sArrays[0],cxt);
        renderDigit(60+93*(RADIUS+1),30,sArrays[1],cxt);
    };
    
    function renderDigit(x,y,num,cxt){
        cxt.fillStyle = "rgb(0,102,153)";
        for (var i = 0; i < digit[num].length; i++) {
            for (var j = 0; j < digit[num][i].length; j++) {
                if(digit[num][i][j]==1){
                    cxt.beginPath();
                    cxt.arc(x+(j*2+1)*(RADIUS+1),y+(i*2+1)*(RADIUS+1),RADIUS,0,2*Math.PI);
                    cxt.closePath();
                    cxt.fill();
                }
            }
        }
    };
    
    function nowTimeShow(){
        var canvas=document.getElementById("digitcanvas");
        var context=canvas.getContext("2d");
        canvas.width=WinWidth;
        canvas.height=WinHeight;
        setInterval(function(){render(context)},500); 
    };